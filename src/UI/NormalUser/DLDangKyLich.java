    public void loadTable() {
        listPhieuDK = fileHandle.getDSPhieuDK();
        //Set header
        tbDSDaDangKy.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
        //Ten cac cot
        String col[] = {"STT","Giáo viên", "Mã lớp", "Mã môn", "Mã phòng", "Ca", "Thứ", "Từ ngày", "Đến ngày"};
        DefaultTableModel model = new DefaultTableModel(col, 0);
        int stt = 0;
        SimpleDateFormat fm = new SimpleDateFormat("dd/MM/yyyy");
        for (PhieuDangKyModel pdk : listPhieuDK) {
            if(!pdk.getTrangThai().equals("Từ chối")) {
                stt++;
                Object[] obj = {stt, pdk.getTk().getHoTen(), pdk.getLh().getMaLop(), pdk.getMh().getMaMH(), 
                pdk.getPth().getMaPhong(), pdk.getCa(), pdk.getThu(), 
                fm.format(pdk.getTuNgay()), fm.format(pdk.getDenNgay())};
                model.addRow(obj);
            }
        }
        
        tbDSDaDangKy.setModel(model);
        
        //Để sắp xếp
        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(tbDSDaDangKy.getModel());
        tbDSDaDangKy.setRowSorter(rowSorter);
    }
    
