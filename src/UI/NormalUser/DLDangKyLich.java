    private void btnDangKyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangKyActionPerformed
        if (datePkTuNgay.getDate() == null || datePkDenNgay.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ");
        } else {
            Date tuNgay = datePkTuNgay.getDate();
            Date denNgay = datePkDenNgay.getDate();
            //Kiểm tra tuNgay phai < denNgay
            if(tuNgay.compareTo(denNgay) >= 0) {
                JOptionPane.showMessageDialog(null, "Ngày bắt đầu hoặc ngày kết thúc không hợp lệ");
            } else {
                PhieuDangKyModel pdk = new PhieuDangKyModel();
                int lastId = JPDangKyLich.jpDangKyLich.getLastId();
                pdk.setMaPhieu(lastId + 1);
                pdk.setTk(new TaiKhoanModel(test.tk.getMaGV(), test.tk.getHoTen()));
                pdk.setLh(new LopHocModel(cboMaLop.getSelectedItem().toString()));
                pdk.setMh(new MonHocModel(cboMaMon.getSelectedItem().toString()));
                String maPhong = cboMaPhong.getSelectedItem().toString();
                String ca = cboCa.getSelectedItem().toString();
                String thu = cboThu.getSelectedItem().toString();
                pdk.setTrangThai("Chờ duyệt");
                //Kiem tra trung lich
                if (JPDangKyLich.jpDangKyLich.isTrungLich(maPhong, ca, thu, tuNgay, denNgay)) {
                    JOptionPane.showMessageDialog(null, "Lịch bị trùng");
                } else {
                    pdk.setPth(new PhongTHModel(maPhong));
                    pdk.setCa(ca);
                    pdk.setThu(thu);
                    pdk.setTuNgay(tuNgay);
                    pdk.setDenNgay(denNgay);
                    JPDangKyLich.jpDangKyLich.themPhieuDangKy(pdk);
                    JOptionPane.showMessageDialog(null, "Đăng ký thành công");
                    JPDangKyLich.jpDangKyLich.loadTable();
                    this.loadTable();
                }
            }
            
        }
    }//GEN-LAST:event_btnDangKyActionPerformed

