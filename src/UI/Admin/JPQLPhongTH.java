    public void loadTable() {
        tbPhongTH.setModel(new PhongTHDAO(listPhongTH));
        //Để sắp xếp bảng
        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(tbPhongTH.getModel());
        tbPhongTH.setRowSorter(rowSorter);
        //Lấy kí tự nhập vào form tìm kiếm
        txtSearch.getDocument().addDocumentListener(new DocumentListener() {
            //Mỗi khi thêm kí tự
            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = txtSearch.getText();
                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                }else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }
            
            //Mỗi khi xóa kí tự
            @Override
            public void removeUpdate(DocumentEvent e) {
                String text = txtSearch.getText();
                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                }else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
            }
        });
        //Set header
        tbPhongTH.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
    }
    
    public void ghiDSPhongTH() {
        fileHandle.ghiDSPhongTH(listPhongTH);
    }
    
    public void themPhongTH(PhongTHModel pth) {
        listPhongTH.add(pth);
        ghiDSPhongTH();
    }
    
    public void suaPhongTH(PhongTHModel pth, int index) {
        listPhongTH.set(index, pth);
        ghiDSPhongTH();
    }
    
    public void xoaPhongTH(int index) {
        listPhongTH.remove(index);
        ghiDSPhongTH();
    }

    public ArrayList<PhongTHModel> getListPhongTH() {
        return listPhongTH;
    }
    

