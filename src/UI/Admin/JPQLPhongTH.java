    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        int selectedRowIndex = tbPhongTH.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn phòng nào !");
        } else {
            String maPhong = tbPhongTH.getValueAt(selectedRowIndex, 1).toString();
            PhongTHModel pthXoa = new PhongTHModel(maPhong);
            
            //Hiển thị xác nhận xóa
            int qs;
            qs = JOptionPane.showConfirmDialog(null, "Xóa phòng có mã: " + maPhong, "Xóa phòng", JOptionPane.YES_NO_OPTION);
            if (qs == JOptionPane.YES_OPTION){
                xoaPhongTH(listPhongTH.indexOf(pthXoa));
                loadTable();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        //Lấy thông tin phòng thực hành đã chọn và mở form sửa khi nhấn nút sửa
        int selectedRowIndex = tbPhongTH.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn phòng nào !");
        } else {
            PhongTHModel pth = new PhongTHModel();
            pth.setMaPhong(tbPhongTH.getValueAt(selectedRowIndex, 1).toString());
            pth.setTenPhong(tbPhongTH.getValueAt(selectedRowIndex, 2).toString());
            pth.setTang((int)tbPhongTH.getValueAt(selectedRowIndex, 3));
            pth.setToaNha(tbPhongTH.getValueAt(selectedRowIndex, 4).toString());
            pth.setCauHinh(tbPhongTH.getValueAt(selectedRowIndex, 5).toString());
            try {
                pth.setSoMay((int)tbPhongTH.getValueAt(selectedRowIndex, 6));
                pth.setTinhTrang(tbPhongTH.getValueAt(selectedRowIndex, 7).toString());
                
                //Truyền pth từ lựa chọn trên bảng vào form sửa
                DLSuaPhongTH dlSuaPhong = new DLSuaPhongTH(test.jfmainAdmin, true, pth, listPhongTH.indexOf(pth));
                dlSuaPhong.setTitle("Sửa thông tin phòng thực hành");
                dlSuaPhong.setVisible(true);
            } catch (Exception e) {
                
            }  
        }

    }//GEN-LAST:event_btnSuaActionPerformed


