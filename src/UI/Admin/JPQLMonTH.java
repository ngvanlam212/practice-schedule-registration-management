    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        
        int selectedRowIndex = tbMonHoc.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn môn nào !");
        } else {
            String maMon = tbMonHoc.getValueAt(selectedRowIndex, 1).toString();
            MonHocModel mhXoa = new MonHocModel(maMon);
            
            //Hiển thị xác nhận xóa
            int qs;
            qs = JOptionPane.showConfirmDialog(null, "Xóa môn: " + maMon, "Xóa môn", JOptionPane.YES_NO_OPTION);
            if (qs == JOptionPane.YES_OPTION){
                xoaMon(listMon.indexOf(mhXoa));
                loadTable();
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        //Lấy thông tin môn thực hành đã chọn và mở form sửa khi nhấn nút sửa
        int selectedRowIndex = tbMonHoc.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn môn học nào !");
        } else {
            MonHocModel mh = new MonHocModel();
            mh.setMaMH(tbMonHoc.getValueAt(selectedRowIndex, 1).toString());
            mh.setTenMH(tbMonHoc.getValueAt(selectedRowIndex, 2).toString());
            try {
                mh.setSoTinChi((int)tbMonHoc.getValueAt(selectedRowIndex, 3));

                //Truyền mh từ lựa chọn trên bảng vào form sửa
                DLSuaMonTH dlSuaMon = new DLSuaMonTH(test.jfmainAdmin, true, mh, listMon.indexOf(mh));
                dlSuaMon.setTitle("Sửa thông tin môn học");
                dlSuaMon.setVisible(true);
            } catch (Exception e) {

            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed


