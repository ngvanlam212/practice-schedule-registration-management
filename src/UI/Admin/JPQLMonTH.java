    public void loadTable() {
        tbMonHoc.setModel(new MonHocDAO(listMon));
        //Để sắp xếp bảng
        TableRowSorter<TableModel> rowSorter = new TableRowSorter<>(tbMonHoc.getModel());
        tbMonHoc.setRowSorter(rowSorter);
        
        //Lấy kí tự nhập vào form tìm kiếm
        txtSearch.getDocument().addDocumentListener(new DocumentListener() {
            //Mỗi khi thêm kí tự
            @Override
            public void insertUpdate(DocumentEvent e) {
                String text = txtSearch.getText();
                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                }else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }
            
            //Mỗi khi xóa kí tự
            @Override
            public void removeUpdate(DocumentEvent e) {
                String text = txtSearch.getText();
                if (text.trim().length() == 0) {
                    rowSorter.setRowFilter(null);
                }else {
                    rowSorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
                }
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
            }
        });
        
        tbMonHoc.getTableHeader().setFont(new Font("Segoe UI", Font.BOLD, 14));
    }
    
    public void ghiDSMon() {
        fileHandle.ghiDSMon(listMon);
    }
    
    public void themMon(MonHocModel mh) {
        listMon.add(mh);
        ghiDSMon();
    }
    
    public void suaMon(MonHocModel mh, int index) {
        listMon.set(index, mh);
        ghiDSMon();
    }
    
    public void xoaMon(int index) {
        listMon.remove(index);
        ghiDSMon();
    }

    public ArrayList<MonHocModel> getListMon() {
        return listMon;
    }
    

